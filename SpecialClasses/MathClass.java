package SpecialClasses;

public class MathClass {
    public static void main(String[] args){
        int n1=10;
        int n2=20;
        System.out.println(Math.max(n1,n2));
        System.out.println(Math.min(n1,n2));
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.pow(n1,n2));
        System.out.println(Math.sqrt(36));
        System.out.println(Math.cbrt(100));
        System.out.println(Math.ceil(10.2));
        System.out.println(Math.floor(10.8));
        System.out.println(Math.round(10.50));
        System.out.println(Math.random()); //returns a value which is >0 && <1
    }
}

